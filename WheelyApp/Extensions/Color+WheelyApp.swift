import SwiftUI

extension Color {
    /// Screen background color
    static let screenBackground: Color = Color("screenBackground")

    /// Bubble background color
    static let bubbleBackground: Color = Color("bubbleBackground")

    /// Primary purple color used for action elements
    static let primaryPurple: Color = Color("primaryPurple")

    /// Secondary purple color used for pressed state
    static let secondaryPurple: Color = Color("secondaryPurple")

    /// Tertiary purple color used for disabled state
    static let tertiaryPurple: Color = Color("tertiaryPurple")

    /// Red color for error elements
    static let errorRed: Color = Color("errorRed")

    /// Dark text color to use on a light background
    static let darkText: Color = Color("darkText")

    /// Light text color to use on a dark background
    static let lightText: Color = Color("lightText")
}
